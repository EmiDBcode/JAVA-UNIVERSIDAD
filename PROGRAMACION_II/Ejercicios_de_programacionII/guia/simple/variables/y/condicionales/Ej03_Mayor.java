package guia.simple.variables.y.condicionales;

public class Ej03_Mayor {

    public static void mayor (int n1, int n2){

        if (n1>n2){
            System.out.println("El primer numero es mayor al segundo");
        }
        else if (n1<n2){
            System.out.println("El segundo numero es menor al primero");
        }
    }
}
